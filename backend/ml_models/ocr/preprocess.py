import re

LATIN_TO_CYRILLIC = {
      'A': 'А', 'B': 'В', 'C': 'С', 'E': 'Е', 'H': 'Н',
      'K': 'К', 'M': 'М', 'O': 'О', 'P': 'Р', 'T': 'Т',
      'X': 'Х', 'Y': 'У', 'a': 'а', 'c': 'с', 'e': 'е',
      'o': 'о', 'p': 'р', 'x': 'х', 'y': 'у'
}

_JUNK_PATTERN = re.compile(
    r'\d+[\s.,]?\s*(?:г|гр|мл|кг|ккал|руб|р|₽|шт|rp|rр|cal|mg|ml|kg|g)\b'
    r'|\d+',
    re.IGNORECASE
)

_STOP_WORDS = {
    'меню', 'завтрак', 'обед', 'ужин', 'полдник', 'перекус', 'ежедневное',
    'столовая', 'ресторан', 'кафе', 'бар', 'буфет', 'бистро', 'кофейня', 'закусочная',
    'название', 'наименование', 'блюдо', 'блюда', 'состав', 'выход', 'цена', 'порция',
    'калорийность', 'калории', 'ккал', 'белки', 'жиры', 'углеводы', 'вес',
    'итого', 'всего', 'сумма', 'стоимость', 'прейскурант', 'позиция',
    'горячее', 'холодное', 'гарнир', 'десерт', 'напиток', 'напитки',
    'салаты', 'супы', 'закуски', 'выпечка', 'соусы', 'другое',
    'первые', 'вторые', 'холодные', 'горячие', 'основные', 'фирменные',
    'детское', 'вегетарианское', 'постное', 'сезонное', 'специальное',
    'продолжение', 'начало', 'конец', 'страница', 'лист', 'раздел',
    'ассортимент', 'предложение', 'акция', 'новинка', 'хит', 'рекомендуем',
}

def normilize_ocr_text(text: str) -> str:
    text = ''.join(LATIN_TO_CYRILLIC.get(ch, ch) for ch in text)
    text = _JUNK_PATTERN.sub('', text)
    text = re.sub(r'[:/;\\|.\[\](){}]', '', text)
    tokens = [t for t in text.split() if re.search(r'[а-яёА-ЯЁ]', t)]
    text = ' '.join(tokens)
    words = text.lower().split()
    if words and all(w in _STOP_WORDS for w in words):
        return ''
    return text